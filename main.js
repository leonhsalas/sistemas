let sistema = prompt("¿ Qué sistema operativo usa ?")

switch (sistema) {
    case "Windows":
        alert("Windows es el sistema operativo insignia de Microsoft, el estándar de facto para las computadoras domésticas y de negocios. El sistema operativo basado en una interfaz gráfica de usuario (GUI) fue introducido en 1985 y se han liberado muchas versiones desde entonces, como se describe a continuación.")
        break;

    case "Linux":
        alert("Linux es un sistema operativo open source gratuito que se lanza en virtud de la Licencia Pública General de GNU (GPL). Cualquier persona puede ejecutar, estudiar, modificar o redistribuir el código fuente, e incluso comercializar las copias de su código modificado, siempre y cuando lo hagan con la misma licencia.")
        break;

    case "Mac":
        alert("MAC es el acrónimo o abreviatura de Media Access Control y se refiere al conjunto de protocolos comunicacionales por medio del cual, varios equipos o dispositivos conectados a la red acuerdan usar conjuntamente un determinado medio de transmisión")
        break;

    default:
        alert("Pon el sistema operativo que usas en mayúsculas la primera letra")
        break;
}

